			<h1>招生信息</h1>
		</div>
	    <p>各省、自治区、直辖市高等学校招生委员会、教育厅（教委），新疆生产建设兵团教育局，有关部门（单位）教育司（局），有关高等学校：</p><p><br/>

为贯彻落实《国务院关于深化考试招生制度改革的实施意见》（国发〔2014〕35号），现就进一步完善和规范高校自主招生试点工作提出如下意见：</p><p><br/>

一、明确工作总体要求。高校自主招生是我国高校考试招生制度的有机组成部分，是对现行统一高考招生录取的一种补充。进一步完善和规范高校自主招生试点工作，要明晰试点定位，主要选拔具有学科特长和创新潜质的优秀学生。突出问题导向，着力解决自主招生中存在的“掐尖”、“小高考”、影响中学教学秩序等问题。促进科学选才，尊重教育规律和人才成长规律，通过科学有效途径选拔特殊人才。维护公平公正，确保机会公平、程序公开、结果公正。要进一步完善招生程序，合理确定考核内容和形式，规范并公开自主招生办法、考核程序和录取结果，严格控制自主招生规模，2015年起自主招生考核安排在全国统一高考后进行。</p><p><br/>

二、完善申请报名和审核程序。考生向试点高校提出申请，考生所在中学（单位）或原毕业中学、社会团体或专家个人等均可实名提供推荐材料并对其真实性负责。试点高校不得向中学分配推荐名额。考生所在中学（单位）或原毕业中学应依据考生学籍档案、在校表现和高校要求，如实提供考生在高中阶段德智体美各方面发展情况，包括高中阶段课程修习情况和相关成绩、学业水平考试成绩、社会公益活动情况、获奖证书证明以及其他反映学生综合素质发展情况的写实性材料。试点高校要组织相关学科专家认真审核考生提交的申请材料，合理确定参加本校考核的考生名单。在保证生源质量的基础上，向中西部地区、农村地区的申请考生适当倾斜。试点高校应依据相关法律法规、规章和本意见的原则要求，结合实际认真研究制定本校年度自主招生简章,明确申请报名条件、审核办法、考核时间等内容，报教育部备案后向社会公布。</p><p><br/>

三、合理确定考核内容和形式。试点高校考核要结合本校相关学科、专业特色及培养要求，确定相应的考核内容，重点考查考生的学科特长、创新潜质。考核由试点高校单独组织，不得采用联考方式或组织专门培训。充分发挥学科专家的作用，探索完善科学、有效、简便、规范的考核方式。如需笔试，考试科目原则上一门、不超过两门。考核过程须全程录像，专家名单和面试顺序由抽签随机确定，防止暗箱操作。对偏远、贫困地区考生，试点高校要积极探索选派专家到当地开展考核、实行网络远程视频面试等方式，为考生顺利参加考试提供便利和帮助。</p><p><br/>

四、规范录取程序和要求。试点高校要根据本校自主招生简章，由校招生工作领导小组集体研究确定入选资格考生、专业及优惠分值。各省级招生考试机构要严格审查考生投档资格。入选考生高考成绩总分录取要求，原则上不应低于考生所在省（区、市）有关高校同批次同科类录取控制分数线。对学科特长或创新潜质特别突出的个别优秀考生，经向社会公示后，由试点高校提出破格录取申请，经生源所在地省级高校招生委员会核准后录取。严格控制自主招生规模，现阶段不扩大试点高校范围和招生比例。</p><p><br/>

五、自主招生考核安排在全国统一高考后进行。2015年起，所有试点高校自主招生考核统一安排在高考结束后、高考成绩公布前进行。2月底前，试点高校发布年度自主招生简章。3月底前，考生完成报名申请。4月底前，试点高校完成考生材料审核，确定参加学校考核考生名单并进行公示。6月7日、8日，考生参加全国统一高考。6月10日至22日，试点高校完成考核，确定入选资格考生名单、专业及优惠分值，并报教育部阳光高考平台公示。各省级招生考试机构公布高考成绩后，组织本省（区、市）有关考生单独填报自主招生志愿，原则上在本科第一批次录取前完成自主招生录取并进行公示。</p><p><br/>

六、加强信息公开公示。完善教育部、各省级招生考试机构、试点高校和中学四级信息公开制度。在教育部阳光高考平台建立统一的自主招生信息管理系统，加强对报名、审核、公示各个环节监督管理。中学要公示所有经确认推荐的考生名单及相关材料。试点高校要将参加考核的考生名单、入选资格考生名单、录取考生名单及相关信息，分别在本校、生源所在省级招生考试机构及教育部阳光高考平台上进行公示。公示的考生信息应包括姓名、性别、所在中学（或单位）、享受照顾政策类别、资格条件、测试项目、测试成绩、合格标准、拟录高校及专业和录取优惠分值等。</p><p><br/>

七、严厉查处各类违规行为。试点高校不得发布未经教育部备案的自主招生简章或进行虚假招生宣传；不得在高考前以任何形式组织与自主招生挂钩的考核工作；高校自主招生工作人员、专家评委不得参与社会机构组织的各类培训、辅导活动；不得以各种形式偏离试点定位进行恶性生源竞争或向考生违规承诺录取；录取时不得突破自主招生计划录取，不得突破经公示的优惠分值录取，不得更改经公示的入选专业录取，不得在发放新生录取通知书或新生入学报到环节更改考生录取专业。省级教育行政部门不得擅自扩大试点高校范围或出台与国家招生政策相抵触的招生办法。省级招生考试机构不得为不符合要求的考生或违反规定程序办理录取手续。有关中学等不得出具与事实不符的考生推荐材料、证明材料等或在考生综合素质档案中虚构事实或故意隐瞒事实。</p><p><br/>

相关部门、机构和学校建立考试录取申诉、举报机制，及时回应处理各种问题。对违规违纪的部门、机构、学校、考生和工作人员，一经查实，要依据《国家教育考试违规处理办法》（教育部令第33号）和《普通高等学校招生违规行为处理暂行办法》（教育部令第36号）严肃处理。涉嫌犯罪的，移送司法机关处理。</p><p><br/>

各省级教育行政部门、招生考试机构、试点高校和有关中学要根据本意见精神，加强领导，密切配合，完善制度，协同推进。省级招生考试机构和有关中学要为试点高校提供必要的支持和服务。建立试点高校动态管理机制和准入退出机制，确保自主招生试点工作平稳有序开展。</p>
